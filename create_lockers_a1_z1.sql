SELECT 
    code AS 'Kode Lemari',
    COUNT(*) AS 'Jumlah Rak',
    SUM(max_capacity) AS 'Total Kapasitas'
FROM lockers
WHERE code LIKE '%1' AND code REGEXP '^[A-Z]1$'
GROUP BY code
ORDER BY CAST(REPLACE(SUBSTRING(code, 2), '.', '') AS UNSIGNED) ASC, SUBSTRING(code, 1, 1) ASC;

-- Total keseluruhan
SELECT 
    COUNT(*) AS 'Total Rak',
    COUNT(DISTINCT code) AS 'Total Lemari',
    SUM(max_capacity) AS 'Total Kapasitas Semua Rak'
FROM lockers
WHERE code LIKE '%1' AND code REGEXP '^[A-Z]1$';

